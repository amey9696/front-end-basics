// while loop
// print 0 to 9 number
let n = 0;
while (n <= 9) {
    console.log(n);
    n++
}

// sum of first 10 number
let num = 1;
let total = 0;
while (num <= 10) {
    total = total + num;
    num++;
}
console.log(total);

