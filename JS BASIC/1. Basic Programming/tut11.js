// booleans and comparison operator

//booleans (return true or false value)
let num1 = 5;
let num2 = 7;
console.log(num1 > num2);
console.log(num2 > num1);

// == vs ===
let n1 = 5;
let n2 = '5';
console.log(n1 == n2); //check values only
console.log(n1 === n2);//check values and datatypes also

// != vs !==
console.log(n1 != n2); //check values only
console.log(n1 !== n2);//check values and datatypes also